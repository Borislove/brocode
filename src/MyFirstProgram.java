public class MyFirstProgram {

    public static void main(String[] args) {
        System.out.print("I love pizza");
        System.out.print("It's really good!");

        System.out.print("I love pizza\n");  //с новой строки
        System.out.print("It's really good!");

        System.out.println("I love pizza\n");
        System.out.println("It's really good!");

        System.out.println("\tI love pizza");  //табуляция

        System.out.println("\"I love pizza\"");  //кавычки

        System.out.println("\\I love pizza");

        //This is a comment
    }
}
